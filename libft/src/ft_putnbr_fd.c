#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	op;

	if (fd < 0)
		return ;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n >= 10)
		ft_putnbr_fd((n / 10), fd);
	op = (n % 10) + '0';
	write(fd, &op, 1);
}
